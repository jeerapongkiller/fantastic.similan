    <script type="text/javascript">
        $(document).ready(function() {
            var jqForm = $('#boat-create-form'),
                picker = $('#dob'),
                dtPicker = $('#dob-bootstrap-val'),
                select = $('.select2');

            // select2
            select.each(function() {
                var $this = $(this);
                $this.wrap('<div class="position-relative"></div>');
                $this
                    .select2({
                        placeholder: 'Select ...',
                        dropdownParent: $this.parent()
                    })
                    .change(function() {
                        $(this).valid();
                    });
            });

            // Picker
            if (picker.length) {
                picker.flatpickr({
                    onReady: function(selectedDates, dateStr, instance) {
                        if (instance.isMobile) {
                            $(instance.mobileInput).attr('step', null);
                        }
                    }
                });
            }

            if (dtPicker.length) {
                dtPicker.flatpickr({
                    onReady: function(selectedDates, dateStr, instance) {
                        if (instance.isMobile) {
                            $(instance.mobileInput).attr('step', null);
                        }
                    }
                });
            }

            // jQuery Validation
            // --------------------------------------------------------------------
            if (jqForm.length) {
                $.validator.addMethod("regex", function(value, element, regexp) {
                    return this.optional(element) || regexp.test(value);
                }, "Please check your input.");

                jqForm.validate({
                    rules: {
                        'name': {
                            required: true,
                            regex: /^[a-zA-Z0-9#]/,
                            remote: {
                                url: "pages/boat/function/check-name.php",
                                type: "post"
                            }
                        },
                        'refcode': {
                            regex: /^[a-zA-Z0-9ก-ฮ#]/,
                            remote: {
                                url: "pages/boat/function/check-refcode.php",
                                type: "post"
                            }
                        },
                        'capacity': {
                            regex: /^[0-9]/
                        },
                    },
                    messages: {
                        'name': {
                            remote: "This name is already taken! Try another."
                        },
                        'refcode': {
                            remote: "This ref code is already taken! Try another."
                        }
                    },
                    submitHandler: function(form) {
                        // update ajax request data
                        var serializedData = $(form).serialize();
                        $.ajax({
                            url: "pages/boat/function/create.php",
                            type: "POST",
                            data: serializedData + "&action=create",
                            success: function(response) {
                                if (response != false && response > 0) {
                                    Swal.fire({
                                        title: "The information has been added successfully.",
                                        icon: "success",
                                    }).then(function(isConfirm) {
                                        if (isConfirm) {
                                            window.location.href = './?pages=boat/edit&id=' + response; // refresh page
                                        }
                                    });
                                } else {
                                    Swal.fire({
                                        title: "Please try again.",
                                        icon: "error",
                                    });
                                }
                            }
                        });
                    }
                });

            }
        });
    </script>
